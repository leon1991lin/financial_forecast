	iso_code varchar(24) comment '國碼',
    continent varchar(24) comment '洲名',
    location varchar(24) comment '國家',
    date_t date comment '統計日期',
    total_cases int default 0 comment '總確診數',
    new_cases int default 0 comment '新增確診數',
    new_cases_smoothed float(10,2) default 0.0 comment '七天移動平均新增確診數',
    total_deaths int default 0 comment '總死亡數',
    new_deaths int default 0 comment '新增死亡數',
    new_deaths_smoothed float(10,2) default 0.0 comment '七天移動平均新增死亡數',
    total_cases_per_million float(10,2) default 0.0 comment '每百萬人確診數',
